package LayerPaint;

class Tuple4d {

    public double x, y, z, w;

    public Tuple4d() {
        x = 0;
        y = 0;
        z = 0;
        w = 0;
    }

    public Tuple4d(double x, double y, double z, double w) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.w = w;
    }
}
